int	ft_is_prime(int nb)
{
	int	check;

	check = 2;
	if (nb < 2)
		return (0);
	while (check != nb)
	{
		if (nb % check == 0)
			return (0);
		if (check == 46341)
			break ;
		check++;
	}
	return (1);
}
